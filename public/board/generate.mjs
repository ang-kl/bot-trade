#!/usr/bin/env node
/* generate.mjs — pull merged PRs from GitHub and write board.data.js.
 *
 *   node generate.mjs [owner/repo]
 *
 * Requires the GitHub CLI (`gh`) installed + authenticated. Reads
 * board.config.json for everything human-authored (branch map, per-PR scores,
 * leads, and the Dimensions/Governance/Docs/Crew tabs); auto-derives the PR
 * tree, the activity feed, the merged-PR count, live version and avg score.
 *
 * Nothing here is project-specific — point project.repo at any repo.
 */
import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";

const cfg = JSON.parse(readFileSync("board.config.json", "utf8"));
const repo = (process.argv[2] || cfg.project.repo || "")
  .replace(/^https?:\/\/github\.com\//, "").replace(/\/$/, "");
if (!/^[\w.-]+\/[\w.-]+$/.test(repo)) {
  console.error("Need a valid owner/repo (arg or project.repo in board.config.json).");
  process.exit(1);
}

console.error(`Fetching merged PRs from ${repo} …`);
let prs;
try {
  // execFileSync + arg array — no shell, so `repo` can't inject anything.
  const out = execFileSync("gh", [
    "pr", "list", "-R", repo, "--state", "merged",
    "--limit", String(cfg.limit || 300),
    "--json", "number,title,mergedAt,body",
  ], { encoding: "utf8", stdio: ["ignore", "pipe", "inherit"] });
  prs = JSON.parse(out);
} catch {
  console.error("gh failed — is the GitHub CLI installed & authenticated? (`gh auth status`)");
  process.exit(1);
}
if (!prs.length) { console.error("No merged PRs found."); process.exit(1); }

const verRe = /v(\d+\.\d+\.\d+)/i;
const verOf = p => { const m = (p.title + " " + (p.body || "")).match(verRe); return m ? "v" + m[1] : ""; };
const cleanTitle = t => t
  .replace(/^\w+(\([^)]*\))?:\s*/, "")           // strip "feat(x): "
  .replace(/\s*\(v?\d+\.\d+\.\d+\).*$/i, "")     // strip trailing "(v0.1.x)…"
  .replace(/\s*\(#\d+\)\s*$/, "")                // strip trailing "(#123)"
  .trim();
const bucket = title => {
  const t = title.toLowerCase();
  for (const b of cfg.branchMap || []) if ((b.kw || []).some(k => t.includes(k))) return b;
  return { branch: cfg.fallbackBranch || "Other", sub: "Merged" };
};

prs.sort((a, b) => a.number - b.number); // oldest → newest = build order

const branches = new Map();
for (const p of prs) {
  const { branch, sub } = bucket(p.title);
  const key = "#" + p.number;
  const row = [key, cleanTitle(p.title), verOf(p), cfg.leads?.[key] || cfg.defaultLead || "", cfg.scores?.[key] ?? null];
  if (!branches.has(branch)) branches.set(branch, new Map());
  const subs = branches.get(branch);
  if (!subs.has(sub)) subs.set(sub, []);
  subs.get(sub).push(row);
}
const tree = [...branches].map(([b, subs]) => ({
  b, e: cfg.branchBlurbs?.[b] || "",
  subs: [...subs].map(([h, rows]) => ({ h, prs: rows })),
}));

const feed = prs.slice(-(cfg.feedCount || 5)).reverse()
  .map(p => [verOf(p) || "#" + p.number, cleanTitle(p.title), ""]);

const scored = prs.map(p => cfg.scores?.["#" + p.number]).filter(v => typeof v === "number");
const avg = scored.length ? Math.round(scored.reduce((a, c) => a + c, 0) / scored.length) : null;
const liveVer = [...prs].reverse().map(verOf).find(Boolean) || "";
const stats = (cfg.build?.stats || []).map(s => ({
  ...s,
  n: s.n === "AUTO" ? String(prs.length)
    : s.n === "AUTO_VER" ? liveVer
    : s.n === "AUTO_SCORE" ? (avg != null ? avg + "%" : "—")
    : s.n,
}));
const leaderboard = (cfg.leaderboard || []).map(r => r.map(v => v === "AUTO" ? prs.length : v));

const BOARD = {
  project: cfg.project,
  build: { stats, lanes: cfg.build?.lanes || [] },
  tree, feed,
  dimensions: cfg.dimensions || { caps: [], rows: [] },
  governance: cfg.governance || {},
  advisory: cfg.advisory || {},
  ledger: cfg.ledger || [],
  docs: cfg.docs || {},
  leaderboardNote: cfg.leaderboardNote,
  leaderboard,
  taskforces: cfg.taskforces || [],
  crew: cfg.crew || [],
};

const banner = `/* board.data.js — GENERATED by generate.mjs from ${repo} (${prs.length} merged PRs).\n * Do NOT hand-edit — change board.config.json and re-run \`node generate.mjs\`. */\n`;
writeFileSync("board.data.js", banner + "window.BOARD = " + JSON.stringify(BOARD, null, 2) + ";\n");
console.error(`Wrote board.data.js — ${prs.length} PRs · ${tree.length} branches · live ${liveVer || "?"} · avg ${avg ?? "—"}%.`);
